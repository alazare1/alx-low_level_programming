#include "main.h"

/**
 * _isupper - a function that checks for uppercase character
 * @c: input
 * Return: 1 if c is uppercase, 0 otherwise
 */

int main(void)
{
        char c = 'A';

        if (_isupper(c))
        {
                return 1;
        }
        else {

               return (0);

        }

}
